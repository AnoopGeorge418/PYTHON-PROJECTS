def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b 

def multiplication(a, b):
    return a * b 

def division(a, b):
    if a == 0 or b == 0:
        return 'Divisible by 0 is not possible'
    else:
        return a / b
    

    
    