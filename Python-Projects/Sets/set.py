# Create two sets of numbers.
# Perform union, intersection, and difference operations on the sets.

num = {1, 2, 3, 4, 5, 65,6,7,7,77, 8,98, 9, 90,1}
num2 = {3, 54, 56, 7,8 ,6, 4,3,2,5, 8,9 ,0}

print(f'Union: {num.union(num2)}')
print(f'Intersection: {num.intersection(num2)}')
print(f'Difference: {num.difference(num2)}')